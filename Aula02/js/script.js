
// criamos a variavel cor para guardar a cor que o usuario ira digitar, usamos tambem, o comando chamado prompt que abre uma caixa
// de mensagem para o usuario digitar a cor 
cor = prompt("digite uma cor para o fundo em ingles")

// a cor digitada pelo usuario ficou na variav el cor e sera usada para alterar a cor de fundo do body do nosso documento
document.body.style.backgroundColor = cor